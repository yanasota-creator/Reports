def process(rows):
    out = []
    for theta, ch1, ch2, F, Tw, Z, note in rows:
        # 生データのF・Zは圧縮を負とする符号付き値。レポートではFを絶対値（締結力の大きさ）
        # として表記するため、Z=ΔF/Δlの符号もそれに合わせて反転する
        # （そうしないと弾性域でFの絶対値が増加しているのにZが負になってしまう）。
        Z_abs = None if Z is None else -Z
        out.append(dict(theta=theta, CH1=ch1, CH2=ch2, F=abs(F), Tr=abs(Tw), Z=Z_abs, note=note))
    return out


root = pathlib.Path(__file__).resolve().parent.parent
data_path = root / "data" / "exp2_nut_rotation_method.csv"

with open(data_path, "w", newline="", encoding="utf-8") as f:
    writer = csv.writer(f)
    for label, d in bolts_raw.items():
        rows = process(d["rows"])
        writer.writerow([label, d["material"], f"強度区分{d['grade']}",
                          f"{d['grade']}{d['material']}", "", "", ""])
        writer.writerow(["回転角θ[°]", "CH1", "CH2", "F[N]", "Tr[Nm]", "Z[N/m]", ""])
        for r in rows:
            zstr = "" if r["Z"] is None else f"{r['Z']:.2f}"
            writer.writerow([r["theta"], r["CH1"], r["CH2"], f"{r['F']:.4f}",
                              f"{r['Tr']:.6f}", zstr, r["note"]])
        writer.writerow([])

