images_dir = pathlib.Path(__file__).resolve().parent.parent / "images"


def plot_metric(key, ylabel, title, filename, fit_line=True):
    """データ点は散布図で示し、fit_line=True のときのみ最小二乗法による
    回帰直線を重ねる（μw・μs は理論上直線関係がないため点のみ）"""
    fig, ax = plt.subplots(figsize=(9, 6))
    for label, d in bolts.items():
        x = np.array(d["Tf"], dtype=float)
        y = np.array(d[key], dtype=float)
        if not fit_line:
            # μw・μs は Tf=0 で 0/0 となり定義されないため原点を除外
            x, y = x[x > 0], y[np.array(d["Tf"]) > 0]
        ax.scatter(x, y, color=d["color"], marker=d["marker"], s=60,
                   zorder=3, label=label)
        if fit_line:
            a, b = np.polyfit(x, y, 1)
            xs = np.linspace(0, x.max(), 50)
            ax.plot(xs, a * xs + b, color=d["color"], linewidth=1.2,
                    linestyle="--", zorder=2)
    ax.grid(True, linestyle="-", alpha=0.4)
    ax.set_xlabel("締付トルク $T_f$ [Nm]")
    ax.set_ylabel(ylabel)
    ax.set_title(title)
    ax.legend(loc="best", fontsize=9)
    fig.tight_layout()
    out = images_dir / filename
    fig.savefig(out, dpi=150)
    plt.close(fig)
    print(f"saved to {out}")


